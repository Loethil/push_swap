#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	r;

	if (!s || !f)
		return ;
	r = 0;
	while (s[r])
	{
		f(r, &s[r]);
		r++;
	}
}
